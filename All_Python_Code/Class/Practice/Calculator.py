class Calculator:
    @staticmethod
    def add(x,y):
        return x + y
    
    @staticmethod
    def sub(x,y):
        return x - y
    
    @staticmethod
    def mul(x,y):
        return x * y
    
    @staticmethod
    def div(x,y):
        return x / y
    
print(Calculator.add(2,4))
print(Calculator.sub(2,4))
print(Calculator.mul(2,4))
print(Calculator.div(2,4))
    
